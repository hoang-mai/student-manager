const db = require('../models');
const { NotFoundError } = require('../utils/apiError');
const { paginateQuery } = require('../utils/response');

const Commander = db.profile;
const User = db.user;
const Student = db.profile;
const YearlyResult = db.yearlyResult;
const TimeTable = db.timeTable;
const CutRice = db.cutRice;
const Achievement = db.achievement;
const YearlyAchievement = db.yearlyAchievement;
const TuitionFee = db.tuitionFee;
const Notification = db.notification;

// ===================== CRUD Cơ bản =====================

const create = async (data) => Commander.create(data);
const getAll = async (query) => paginateQuery(Commander, query, {
  filterFields: ['code', 'fullName', 'gender', 'unit', 'rank', 'organization'],
  include: [{ model: User, attributes: { exclude: ['password', 'refreshToken'] } }],
});

const getDetail = async (id) => {
  const record = await Commander.findByPk(id, {
    include: [{ model: User, attributes: { exclude: ['password', 'refreshToken'] } }],
  });
  if (!record) throw new NotFoundError('Không tìm thấy chỉ huy');
  return record;
};

const update = async (id, data) => {
  const record = await getDetail(id);
  return record.update(data);
};

const deleteRecord = async (id) => {
  const record = await getDetail(id);
  await record.destroy();
  return { deleted: true };
};

// ===================== CH-06: Tự động sinh lịch cắt cơm =====================

const generateCutRice = async (userId) => {
  const timetables = await TimeTable.findAll({ where: { userId } });
  if (!timetables.length) throw new NotFoundError('Không có thời khóa biểu để tạo lịch cắt cơm');

  const weekly = {};
  for (const tt of timetables) {
    const schedules = tt.schedules;
    if (!schedules || !Array.isArray(schedules)) continue;

    for (const s of schedules) {
      const day = s.day;
      if (!day) continue;

      if (!weekly[day]) weekly[day] = { morning: false, noon: false, afternoon: false, evening: false };

      const startHour = parseInt(s.startTime?.split(':')[0]) || 0;
      const endHour = parseInt(s.endTime?.split(':')[0]) || 0;

      if (startHour < 10 || endHour <= 10) weekly[day].morning = true;
      if ((startHour >= 10 && startHour < 13) || (endHour > 10 && endHour <= 13)) weekly[day].noon = true;
      if ((startHour >= 13 && startHour < 17) || (endHour > 13 && endHour <= 17)) weekly[day].afternoon = true;
      if (startHour >= 17 || endHour >= 17) weekly[day].evening = true;
    }
  }

  let record = await CutRice.findOne({ where: { userId } });
  if (record) {
    await record.update({ weekly, isAutoGenerated: true, lastUpdated: new Date() });
  } else {
    record = await CutRice.create({ userId, weekly, isAutoGenerated: true, lastUpdated: new Date() });
  }

  const user = await User.findByPk(userId);
  if (user) {
    await Notification.create({
      userId: user.id,
      title: 'Lịch cắt cơm đã được cập nhật',
      content: 'Lịch cắt cơm tuần này đã được cập nhật tự động từ thời khóa biểu.',
      type: 'CUT_RICE',
    });
  }

  return record;
};

const generateAllCutRice = async () => {
  const students = await Student.findAll({ attributes: ['id'] });
  let success = 0, skipped = 0;

  for (const s of students) {
    try {
      const timetables = await TimeTable.findAll({ where: { userId: s.id } });
      if (!timetables.length) { skipped++; continue; }

      const weekly = {};
      for (const tt of timetables) {
        const schedules = tt.schedules;
        if (!schedules || !Array.isArray(schedules)) continue;

        for (const sch of schedules) {
          const day = sch.day;
          if (!day) continue;

          if (!weekly[day]) weekly[day] = { morning: false, noon: false, afternoon: false, evening: false };

          const startHour = parseInt(sch.startTime?.split(':')[0]) || 0;
          const endHour = parseInt(sch.endTime?.split(':')[0]) || 0;

          if (startHour < 10 || endHour <= 10) weekly[day].morning = true;
          if ((startHour >= 10 && startHour < 13) || (endHour > 10 && endHour <= 13)) weekly[day].noon = true;
          if ((startHour >= 13 && startHour < 17) || (endHour > 13 && endHour <= 17)) weekly[day].afternoon = true;
          if (startHour >= 17 || endHour >= 17) weekly[day].evening = true;
        }
      }

      let record = await CutRice.findOne({ where: { userId: s.id } });
      if (record) {
        await record.update({ weekly, isAutoGenerated: true, lastUpdated: new Date() });
      } else {
        await CutRice.create({ userId: s.id, weekly, isAutoGenerated: true, lastUpdated: new Date() });
      }

      await Notification.create({
        userId: s.id,
        title: 'Lịch cắt cơm đã được cập nhật',
        content: 'Lịch cắt cơm tuần này đã được cập nhật tự động.',
        type: 'CUT_RICE',
      });
      success++;
    } catch (e) {
      skipped++;
    }
  }

  return { success, skipped };
};

